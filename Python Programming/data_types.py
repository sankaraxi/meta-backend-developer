# Numbers
# 1. int
# 2. float
# 3. complex
# 4. bool

#examples
# int
x = 1
print(x)
print(type(x))
#output: type of x is <class 'int'>

# float
x = 1.0
print(x)
print(type(x))
#output: type of x is <class 'float'>

# complex
x = 1 + 2j
print(x)
print(type(x))
#output: type of x is <class 'complex'>

# bool
x = True
print(x)
print(type(x))
#output: type of x is <class 'bool'>



# Strings
# 1. single line string
# 2. multi line string
# 3. string formatting


# Examples

# single line string
x = 'Hello World'
print(x)
print(type(x))
#output: type of x is <class 'str'>

# multi line string
x = '''Hello
World'''
print(x)
print(type(x))
#output: type of x is <class 'str'>

# string formatting
name = 'Sankar'
age = 21
x = 'My name is {} and I am {} years old'.format(name, age) #using format method
print(x)
#output: My name is Sankar and I am 21 years old

